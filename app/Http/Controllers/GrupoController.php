    //Función que retorna un JSON con todos los módulos para que los maneje AJAX del lado del servidor
    public function apiGrupos()
    {
        $permiso_editar = Auth::user()->can('grupos.edit');;
        $permiso_eliminar = Auth::user()->can('grupos.destroy');;
        $grupo = Grupo::all();

        if ($permiso_editar) {
            if ($permiso_eliminar) {
                return Datatables::of($grupo)
                ->addColumn('action', function($grupo){
                    return '<a onclick="editForm('. $grupo->id .')" class="btn btn-warning btn-sm"><i class="fa fa-pencil-square-o"></i> Editar</a> ' .
                           '<a onclick="deleteData('. $grupo->id .')" class="btn btn-danger btn-sm"><i class="fa fa-trash-o"></i> Eliminar</a>';
                })->make(true);
            } else {
                return Datatables::of($grupo)
                ->addColumn('action', function($grupo){
                    return '<a onclick="editForm('. $grupo->id .')" class="btn btn-warning btn-sm"><i class="fa fa-pencil-square-o"></i> Editar</a> ' .
                           '<a class="btn btn-danger btn-sm" disabled><i class="fa fa-trash-o"></i> Eliminar</a>';
                })->make(true);
            }
        } elseif ($permiso_eliminar) {
            return Datatables::of($grupo)
            ->addColumn('action', function($grupo){
                return '<a class="btn btn-warning btn-sm" disabled><i class="fa fa-pencil-square-o"></i> Editar</a> ' .
                       '<a onclick="deleteData('. $grupo->id .')" class="btn btn-danger btn-sm"><i class="fa fa-trash-o"></i> Eliminar</a>';
            })->make(true);
        } else {
            return Datatables::of($grupo)
            ->addColumn('action', function($grupo){
                return '<a class="btn btn-warning btn-sm" disabled><i class="fa fa-pencil-square-o"></i> Editar</a> ' .
                       '<a class="btn btn-danger btn-sm" disabled><i class="fa fa-trash-o"></i> Eliminar</a>';
            })->make(true);
        }
    }
